mb44.keymap = function() {
    var keymap = [];
    var layer = [];
    var ks;
    var zones = mb44.zones;
    var i;

    for (i in mb44.keySections) {
        ks = mb44.keySections[i];
        layer = new Array();
        layer.push(ks.row1);
        layer.push(ks.row2);
        layer.push(ks.row3);
        layer.push(ks.bottomRow[zones[0].value].concat(ks.row4));
        keymap.push(layer);
    }

    return keymap;
};
